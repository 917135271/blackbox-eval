def find_violation(dept_id, records, cum_before_val):
    budget = budgets[dept_id]["budget"]
    cum = cum_before_val
    for rid, rdate, amt in records:
        cum += amt
        if cum > budget:
            return (rid, rdate, round(cum, 2), amt)
    return None

