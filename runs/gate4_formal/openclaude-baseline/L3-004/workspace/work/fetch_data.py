# Find crossing month for each department
crossing = {}
for dept, budget in budgets.items():
    cum = 0
    for m in range(1, 13):
        month_key = f"2025-{m:02d}"
        cum += monthly.get((dept, month_key), 0)
        if cum > budget and dept not in crossing:
            crossing[dept] = month_key
            break

